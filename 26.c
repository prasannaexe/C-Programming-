// program to show the difference between writing updation statement before and after output 
#include<stdio.h>
int main (){
    int i =1;
    while (i<=5){
        printf ("%d",i);
        i++;
    }
    int j =1;
    while (j<=5){
        j++;
        printf ("%d",j);
        
    }
return 0;
}