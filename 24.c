//increament or decreament shorthand operators
#include <stdio.h>
int main(){
    int i=5 ;
    printf ("%d \n",i++);
    printf ("%d\n",i);
    int a=10;
    printf ("%d\n",++a);
    printf ("%d\n",a);
    int b=15;
    printf ("%d\n",b--);
    printf ("%d\n",b);
    int c=20;
    printf ("%d\n",--c);
    printf ("%d\n",c);
    return 0;
    
}